import { generateKeyPairSync } from "node:crypto";
import { mkdir, readFile, stat, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";
import { Buffer } from "node:buffer";
import process from "node:process";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(packageRoot, "..");
const appData = process.env.APPDATA;
if (!appData) throw new Error("Windows APPDATA is unavailable.");
const userData = join(appData, "WholesalePOS License Manager");

async function exists(path) { try { await stat(path); return true; } catch { return false; } }

function protectWithDpapi(privateKeyPem) {
  const command = "Add-Type -AssemblyName System.Security;$value=[Console]::In.ReadToEnd();$bytes=[Convert]::FromBase64String($value);$protected=[System.Security.Cryptography.ProtectedData]::Protect($bytes,$null,[System.Security.Cryptography.DataProtectionScope]::CurrentUser);[Console]::Out.Write([Convert]::ToBase64String($protected));[Array]::Clear($bytes,0,$bytes.Length)";
  const input = Buffer.from(privateKeyPem, "utf8");
  const result = spawnSync("powershell.exe", ["-NoProfile", "-NonInteractive", "-Command", command], { input: input.toString("base64"), maxBuffer: 1024 * 1024 });
  input.fill(0);
  if (result.status !== 0 || !result.stdout.length) throw new Error(`Windows DPAPI protection failed: ${result.stderr.toString("utf8").trim()}`);
  return result.stdout.toString("utf8").trim();
}

const bootstrapPath = join(userData, "authority.bootstrap.json");
const vaultPath = join(userData, "license-manager.wposvault");
try {
  if (await exists(vaultPath)) throw new Error("A License Manager vault already exists. Authority rotation is intentionally blocked.");
  let bootstrap;
  if (await exists(bootstrapPath)) {
    bootstrap = JSON.parse(await readFile(bootstrapPath, "utf8"));
  } else {
    const { privateKey, publicKey } = generateKeyPairSync("ec", { namedCurve: "prime256v1" });
    const privateKeyPem = privateKey.export({ type: "pkcs8", format: "pem" }).toString();
    bootstrap = {
      format: "wholesalepos-license-authority-bootstrap",
      version: 1,
      publicKeyJwk: publicKey.export({ format: "jwk" }),
      protection: "windows-dpapi-current-user",
      encryptedPrivateKey: protectWithDpapi(privateKeyPem),
      createdAt: new Date().toISOString()
    };
    await mkdir(dirname(bootstrapPath), { recursive: true });
    await writeFile(bootstrapPath, JSON.stringify(bootstrap), { encoding: "utf8", mode: 0o600 });
  }
  const source = `// Generated by scripts/provision-authority.mjs. It contains no private key material.\nexport const AUTHORITY_PUBLIC_KEY_JWK: JsonWebKey = ${JSON.stringify(bootstrap.publicKeyJwk, null, 2)};\n`;
  await writeFile(join(packageRoot, "src", "shared", "authority-public-key.ts"), source, "utf8");
  await writeFile(join(repoRoot, "android-pos-app", "src", "domain", "license-public-key.ts"), source.replace("AUTHORITY_PUBLIC_KEY_JWK", "LICENSE_PUBLIC_KEY_JWK"), "utf8");
  process.stdout.write(`Provisioned authority public key and DPAPI-protected private bootstrap at ${bootstrapPath}\n`);
} finally {
  // The private key is never written to the repository or an unencrypted temporary file.
}
